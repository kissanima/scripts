def get_theme(theme_name):
    """Get theme configuration by name"""
    return THEMES.get(theme_name, THEMES['dark'])

def get_theme_names():
    """Get list of available theme names"""
    return list(THEMES.keys())

def get_theme_display_names():
    """Get list of theme display names"""
    return [theme['name'] for theme in THEMES.values()]

def get_status_color(theme, status):
    """Get color based on status with theme support"""
    colors = {
        'running': theme['health_good'],
        'stopped': theme['text_muted'],
        'warning': theme['health_warning'],
        'error': theme['health_critical'],
        'starting': theme['warning'],
        'stopping': theme['warning'],
        'excellent': theme['health_excellent'],
        'good': theme['health_good'],
        'critical': theme['health_critical'],
        'unknown': theme['health_unknown'],
        'success': theme['success'],
        'info': theme['info']
    }
    return colors.get(status, theme['text_primary'])

def get_all_theme_names():
    """Get list of all available theme names (alias for get_theme_names)"""
    return get_theme_names()
